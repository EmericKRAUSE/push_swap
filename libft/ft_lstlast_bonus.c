#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*temp;

	temp = NULL;
	if (lst != NULL)
	{
		temp = lst;
		while (temp-> next)
		{
			temp = temp-> next;
		}
	}
	return (temp);
}
